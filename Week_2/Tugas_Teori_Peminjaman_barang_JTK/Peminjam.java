public class Peminjam{
    private String nama;
    private String NIM;

    //Constructor
    public Peminjam(String nama, String NIM){
        this.nama = nama;
        this.NIM = NIM;
    }

    //Getter and Setter
    public String getNama(){
        return nama;
    }

    public void setNama(String nama){
        this.nama = nama;
    }

    public String getNIM(){
        return NIM;
    }

    public void setNIM(String NIM){
        this.NIM = NIM;
    }

    //Method
    
}